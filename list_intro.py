# Python Lists: Creating, Indexing, and Slicing Lists
computer_parts = ["computer",
                  "monitor",
                  "keyboard",
                  "mouse",
                  "mouse mat"
                  ]

for part in computer_parts:
    print(part)

print()
print(computer_parts[2])
print(computer_parts[4:0:-1])
print(computer_parts[-1])
print(computer_parts[-1:-5:-1])
